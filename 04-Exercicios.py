#04 - Faça um Programa que peça as 4 notas bimestrais e mostre a média.

nota01=float(input("Digite a Primeira nota: "))
nota02=float(input("Digite a Segunda nota: "))
nota03=float(input("Digite a Terceira nota: "))
nota04=float(input("Digite a Quarta nota: "))
média = (nota01+nota02+nota03+nota04)/ 4
print("A média das notas é {}".format(média))