# 11-Faça um Programa que peça 2 números inteiros e um número real. Calcule e mostre:

a = int(input("Digite o primeiro numero interiro: "))
b = int(input("Digite o segundo numero interiro: "))
c = float(input("Digite o  numero real: "))
produto = (2 * a) * (b / 2)
soma = ((3 * a) + b)
cubo = (c ** 3)
print("O produto é: {}".format(produto))
print("A Soma  é: {}".format(soma))
print("O Cubo é: {}".format(cubo))